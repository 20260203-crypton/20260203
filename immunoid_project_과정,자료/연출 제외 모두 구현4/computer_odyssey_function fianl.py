# Boss 클래스
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class Boss:
    SIZE = 16 * SCALE * 2  # 보스는 일반 몬스터의 2배 크기

    def __init__(self, canvas, imgs_live, imgs_dead, x, y):
        self.canvas     = canvas
        self.x          = float(x)
        self.y          = float(y)
        self.hp         = BOSS_MAX_HP
        self.max_hp     = BOSS_MAX_HP
        self.alive      = True
        self.dying      = False       # 죽음 애니메이션 진행 중
        self.phase      = 1

        self.imgs_live  = imgs_live   # [frame0..frame3] idle/walk 4프레임
        self.imgs_dead  = imgs_dead   # [frame4, frame5] 죽음 2프레임
        self.anim_tick  = 0
        self.anim_frame = 0
        self.dead_frame = 0           # 죽음 애니 인덱스
        self.dead_tick  = 0

        # 타이머
        self.last_shoot_t    = 0.0
        self.last_laser_t    = 0.0
        self.last_tp_t       = 0.0
        self.laser_angle     = 0.0   # 현재 레이저 각도 (순차 회전)
        self.laser_active    = False
        self.laser_timer     = 0.0
        self.laser_items     = []    # 캔버스 라인 id

        # 캔버스 아이템
        self.item  = canvas.create_image(int(x), int(y), anchor="nw",
                                         image=imgs_live[0])
        # HP 바 (보스용 – 상단에 크게)
        self.hp_bg = canvas.create_rectangle(0,0,1,1, fill="#333333", outline="")
        self.hp_fg = canvas.create_rectangle(0,0,1,1, fill="#cc2222", outline="")
        self.hp_lbl = canvas.create_text(0,0, text="BOSS", fill="#ff4444",
                                         font=("Courier", 9, "bold"))
        self._update_hp_bar()

    # ── HP 바 ──────────────────────────────────────────────────
    def _update_hp_bar(self):
        bw = self.SIZE * 2
        bh = 6
        bx = int(self.x) - self.SIZE // 2
        by = int(self.y) - 14
        ratio = max(0, self.hp / self.max_hp)
        self.canvas.coords(self.hp_bg, bx, by, bx+bw, by+bh)
        self.canvas.coords(self.hp_fg, bx, by, bx+int(bw*ratio), by+bh)
        self.canvas.coords(self.hp_lbl, bx+bw//2, by-8)
        # 2페이즈면 HP 바 주황색
        col = "#ff6600" if self.phase == 2 else "#cc2222"
        self.canvas.itemconfig(self.hp_fg, fill=col)

    # ── 애니메이션 업데이트 ──────────────────────────────────────
    def update_anim(self):
        if self.dying:
            self.dead_tick += 1
            if self.dead_tick >= 30:   # 30틱(0.5초)마다 다음 프레임
                self.dead_tick = 0
                if self.dead_frame < len(self.imgs_dead) - 1:
                    self.dead_frame += 1
            self.canvas.itemconfig(self.item, image=self.imgs_dead[self.dead_frame])
        else:
            # 평소: imgs_live[0] 고정 표시
            self.canvas.itemconfig(self.item, image=self.imgs_live[0])

    # ── 피격 ───────────────────────────────────────────────────
    def hit(self, dmg):
        if self.dying: return
        self.hp -= dmg
        if self.hp <= BOSS_PHASE2_HP and self.phase == 1:
            self.phase = 2
        if self.hp <= 0:
            self.hp = 0
            self._start_die()

    def _start_die(self):
        self.dying    = True
        self.alive    = False
        self.dead_frame = 0
        self.dead_tick  = 0
        self._clear_laser()

    # ── 레이저 관리 ────────────────────────────────────────────
    def _clear_laser(self):
        for lid in self.laser_items:
            self.canvas.delete(lid)
        self.laser_items = []
        self.laser_active = False

    # ── 위치 동기화 ────────────────────────────────────────────
    def sync(self):
        self.canvas.coords(self.item, int(self.x), int(self.y))
        self._update_hp_bar()
        self.canvas.tag_raise(self.hp_bg)
        self.canvas.tag_raise(self.hp_fg)
        self.canvas.tag_raise(self.hp_lbl)
        self.canvas.tag_raise(self.item)

    # ── 정리 ───────────────────────────────────────────────────
    def die(self):
        self.canvas.delete(self.item)
        self.canvas.delete(self.hp_bg)
        self.canvas.delete(self.hp_fg)
        self.canvas.delete(self.hp_lbl)
        self._clear_laser()

    def rect(self):
        return (self.x, self.y, self.x+self.SIZE, self.y+self.SIZE)


