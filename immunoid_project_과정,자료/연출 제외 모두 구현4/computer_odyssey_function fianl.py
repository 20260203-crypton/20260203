# Monster 클래스
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class Monster:
    SIZE = 16 * SCALE

    def __init__(self, canvas, img, x, y, hp, spawn_x, spawn_y, is_type2=False):
        self.canvas   = canvas
        self.x        = float(x)
        self.y        = float(y)
        self.hp       = hp
        self.max_hp   = hp
        self.alive    = True
        self.chasing  = False
        self.is_type2 = is_type2   # True면 Monster2(빨간), False면 Monster1(보라)
        # 배회 중심
        self.wander_cx = float(spawn_x)
        self.wander_cy = float(spawn_y)
        self.wander_angle = random.uniform(0, math.pi * 2)
        self.wander_timer = 0.0

        self.item = canvas.create_image(int(x), int(y), anchor="nw", image=img)
        # HP 바
        self.hp_bg = canvas.create_rectangle(0,0,1,1, fill="#333333", outline="")
        self.hp_fg = canvas.create_rectangle(0,0,1,1, fill="#ee4444", outline="")
        self._update_hp_bar()
        # Monster2 방향/애니메이션 상태
        self.facing    = "right"   # "left" or "right"
        self.anim_tick = 0         # 개별 애니메이션 카운터
        self.anim_frame = 0        # 현재 표시 프레임 (0 or 1)

    def _update_hp_bar(self):
        bw = self.SIZE
        bh = 4
        bx = int(self.x)
        by = int(self.y) - 6
        ratio = max(0, self.hp / self.max_hp)
        self.canvas.coords(self.hp_bg, bx, by, bx+bw, by+bh)
        self.canvas.coords(self.hp_fg, bx, by, bx+int(bw*ratio), by+bh)

    def move_toward(self, tx, ty, speed, bounds):
        dx = tx - (self.x + self.SIZE/2)
        dy = ty - (self.y + self.SIZE/2)
        dist = math.hypot(dx, dy)
        if dist > 1:
            nx = dx/dist * speed
            ny = dy/dist * speed
            new_x = max(bounds[0], min(bounds[2], self.x + nx))
            new_y = max(bounds[1], min(bounds[3], self.y + ny))
            self.x, self.y = new_x, new_y
            # 방향 갱신 (수평 이동이 있을 때만)
            if abs(dx) > 0.5:
                self.facing = "right" if dx > 0 else "left"

    def wander(self, speed, bounds):
        self.wander_timer -= 1
        if self.wander_timer <= 0:
            self.wander_angle = random.uniform(0, math.pi*2)
            self.wander_timer = random.randint(30, 90)
        dx = math.cos(self.wander_angle) * speed * 0.5
        dy = math.sin(self.wander_angle) * speed * 0.5
        # 배회 중심에서 너무 멀어지면 중심 방향으로
        dist_c = math.hypot(self.x - self.wander_cx, self.y - self.wander_cy)
        if dist_c > MONSTER_WANDER_RANGE:
            adx = self.wander_cx - self.x
            ady = self.wander_cy - self.y
            ad  = math.hypot(adx, ady)
            if ad > 0:
                dx += (adx/ad) * speed * 0.3
                dy += (ady/ad) * speed * 0.3
        new_x = max(bounds[0], min(bounds[2], self.x + dx))
        new_y = max(bounds[1], min(bounds[3], self.y + dy))
        self.x, self.y = new_x, new_y
        # 방향 갱신
        if abs(dx) > 0.01:
            self.facing = "right" if dx > 0 else "left"

    def sync(self):
        self.canvas.coords(self.item, int(self.x), int(self.y))
        self._update_hp_bar()
        self.canvas.tag_raise(self.hp_bg)
        self.canvas.tag_raise(self.hp_fg)
        self.canvas.tag_raise(self.item)

    def hit(self, dmg):
        self.hp -= dmg
        if self.hp <= 0:
            self.die()

    def die(self):
        self.alive = False
        self.canvas.delete(self.item)
        self.canvas.delete(self.hp_bg)
        self.canvas.delete(self.hp_fg)

    def rect(self):
        return (self.x, self.y, self.x+self.SIZE, self.y+self.SIZE)

    def overlaps(self, other):
        ax1,ay1,ax2,ay2 = self.rect()
        bx1,by1,bx2,by2 = other.rect()
        return not (ax2 < bx1 or bx2 < ax1 or ay2 < by1 or by2 < ay1)


